    <p>Hello! I’m Nate Walker.</p>

    <p>I am a New York based designer and a graduate of Binghamton University with a BFA in graphic design and an MBA in marketing. As a child, I became bored very quickly, leading me to pursue a variety of outlets to express my creativity and challenge my mind. In high school, this pursuit eventually led me to an artistic interest in comics and animation. Driven by my love of conceptualization and problem-solving, I began my studies in college as a physics major. However, after exposure to design theory classes, I realized that I could better apply these passions by combining them with my fervor for creativity. This led me to pursue a Bachelor’s in Fine Art. From a desire to learn more about design’s role in industry, I carried on my education to pursue a Master’s in Business Administration with a focus in marketing. </p>

    <p>Engagement is what inspires my work. By conceptualizing the utility of a piece and taking into account all the external variables it is affected by, from an individual to a global level, I am able to forecast consumer reactions and optimize work that best communicates the primary message it was created for. From this practice, I have found success in roles involving marketing design, advertisement, and social media content creation.</p>

